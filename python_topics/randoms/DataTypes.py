#string types
x="Hello World"
print(x)
#integer types
x=100
print(x)
#float types
x=20.5
print(x)
#complex types
x=1j
print(x)
#list types
x=["Hello", "World"]
print(x)
#tuples
x=("apple","ball")
print(x)
#range types
x=range(6)
print(x)
#dict types
x={"Hello":" World"}
print(x)
#set types
x={"apples","mangoes"}
print(x)
#frozenset
x=frozenset({"apples","mangoes"})
print(x)
#boolean types
x=True
print(x)
#bytes type
x=bytes(5)
print(type(x))
#bytesarray
x=bytearray(5)
print(type(x))
#memoryview
x=memoryview(bytes(5))
print(x)
